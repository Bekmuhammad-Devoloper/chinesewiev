export function getWordEmoji(hanzi: string, translation: string): string {
  // 1) Hanzi bo'yicha aniq moslik
  if (hanziMap[hanzi]) return hanziMap[hanzi];

  // 2) Uzbekcha tarjima bo'yicha (lowercase)
  const lower = translation.toLowerCase().trim();
  if (translationMap[lower]) return translationMap[lower];

  // 3) Tarjimada kalit so'z qidirish
  for (const [key, emoji] of Object.entries(translationMap)) {
    if (lower.includes(key) || key.includes(lower)) return emoji;
  }

  // 4) Agar hech narsa topilmasa — hanzi'ning o'zi emoji sifatida
  return hanzi;
}
